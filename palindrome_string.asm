LEA DI,STRING
XOR AX,AX
L1: CMP [SI],24H
JE L2
ADD AX,1
ADD SI,1
JMP L1

L2:
MOV BX,2
DIV BX
MOV CX,AX
DEC SI

LEVEL: MOV BL,[DI]
;XCHG [SI],BL
;MOV [DI],BL
CMP BL,[SI]
JE INCREAMENT
JNE NO_OPERATION
   
NO_OPERATION:
JMP NOT_PALINDROME 

INCREAMENT:
INC DX
DEC SI
INC DI
LOOP LEVEL

CMP DX,AX
JE PALINDROME    
NOT_PALINDROME:
MOV DX,0H
HLT
PALINDROME: MOV DX,1H
HLT


